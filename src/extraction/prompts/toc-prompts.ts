export const EXTRACT_TOC_SYSTEM = `Ты — аналитик структуры книги. На следующих страницах напечатано
оглавление учебника или научной книги.

Задача:
1. Распознай все элементы оглавления (главы, разделы, подразделы)
2. Определи иерархию (что является главой, что — разделом)
3. Определи номер начальной страницы для каждого элемента
4. Переведи все заголовки на русский язык (если книга на другом языке)

Формат ответа — JSON массив:
[
  {
    "title": "Название на русском",
    "page": 15,
    "level": 1,
    "parentTitle": "Название родительской главы"
  }
]

level: 1 = глава, 2 = раздел, 3 = подраздел
parentTitle — указывай только для level > 1 (название родительской главы для связи)
page — номер страницы (1-based), как напечатано в книге

Правила:
- НЕ включай элементы оглавления, не являющиеся содержательными разделами
  (предисловие, список литературы, предметный указатель можно пропустить
  или включить как level 1)
- Если номер страницы указан римскими цифрами — конвертируй в арабские
- Если оглавление двухколоночное — объедини в один список
- Сортируй по порядку в книге
- Игнорируй номера глав/разделов в заголовках
  (например, «3. Пределы» → title: «Пределы», не «3. Пределы»)

Верни ТОЛЬКО JSON массив, без markdown обёрток и пояснений.`;

export function extractTocUserText(text: string, pages: [number, number]): string {
  return `Ниже — текст со страниц ${pages[0]}–${pages[1]} книги. Это оглавление.

Распознай все элементы оглавления и верни JSON массив.

--- ТЕКСТ ОГЛАВЛЕНИЯ ---
${text}
--- КОНЕЦ ---`;
}

export function extractTocUserVision(pageNumbers: number[]): string {
  return `На изображении(-ях) — страница(-ы) ${pageNumbers.join(', ')} книги с оглавлением.
Распознай все элементы оглавления и верни JSON массив по спецификации.`;
}

export const SUMMARIZE_CHAPTER_SYSTEM = `Ты — аналитик знаний. Дано название главы учебника или научной книги.

Задача: напиши краткое описание содержания этой главы (2-3 предложения).

Описание должно:
- Передавать суть главы, а не просто перефразировать заголовок
- Упоминать ключевые понятия и идеи, которые будут рассмотрены
- Помогать читателю понять, какие знания он получит из этой главы
- Быть на русском языке (даже если название на другом языке)

Формат: чистый текст (2-3 предложения), без JSON, без markdown.`;

export function summarizeChapterUser(title: string): string {
  return `Глава: «${title}»`;
}

export const CHAPTER_CONTEXT_INSTRUCTION = `

Дополнительный контекст:
При извлечении идей учитывай контекст текущей главы. Идеи должны быть связаны с тематикой главы и формулироваться с учётом её содержания.`;

export function chapterContextBlock(chapterTitle: string, pageFrom: number, pageEnd: number, summary?: string): string {
  let ctx = `Контекст главы: «${chapterTitle}» (стр. ${pageFrom}–${pageEnd})`;
  if (summary) {
    ctx += `\nСодержание: ${summary}`;
  }
  ctx += CHAPTER_CONTEXT_INSTRUCTION;
  return ctx;
}
