/**
 * System prompt for OCR phase: convert page image to structured Markdown
 * with proper LaTeX formulas, preserving structure.
 */
export const OCR_TO_MARKDOWN_SYSTEM = `Ты — высокоточный OCR-специалист по научным и учебным текстам. Твоя задача — конвертировать изображение страницы книги в чистый Markdown с корректными LaTeX-формулами.

Правила:

1. Текст: Переводи весь текст в Markdown, сохраняя структуру (заголовки, абзацы, списки, нумерация).
2. Формулы в строке (inline): оборачивай в $...$ (одинарные доллары).
3. Формулы на отдельной строке (display): оборачивай в $$...$$ (двойные доллары).
4. Математические обозначения: используй стандартную LaTeX-нотацию.
   - Примеры: \\alpha, \\beta, \\int_0^1, \\frac{a}{b}, \\sqrt{x}, \\sum_{i=1}^n
5. Если формула частично повреждена на изображении — восстанови её по контексту.
6. Нумерацию формул сохраняй: (1), (2) и т.д.
7. Определения, теоремы, леммы — выделяй **жирным** с соответствующим маркером.
8. Не добавляй комментариев и пояснений — только результат конвертации.
9. Если текст на языке отличном от русского — всё равно выводи на языке оригинала (перевод будет на этапе извлечения идей).

Верни только Markdown-текст без обёрток в кодовые блоки.`;

/**
 * User message for single-page OCR.
 */
export function ocrPageUserPrompt(pageNumber: number): string {
  return `Конвертируй содержимое страницы ${pageNumber} в Markdown с LaTeX-формулами.`;
}
