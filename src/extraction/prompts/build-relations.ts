export const BUILD_RELATIONS_SYSTEM = `Ты — аналитик знаний. Дан список идей, извлечённых из книги.

Определи связи между ними. Типы связей:
- prerequisite: идея A необходима для понимания идеи B (A → prerequisite → B)
- elaborates: идея A развивает, уточняет или детализирует идею B
- contradicts: идея A противоречит идее B
- analogous: идея A аналогична идее B (из другой области или контекста)
- applies: идея A применяется в контексте идеи B

Правила:
- Связывай только явно связанные идеи
- Не создавай избыточные связи (5-10 связей на 10 идей — норма)
- Каждая связь должна иметь краткое описание на русском

Верни JSON-объект с полем "relations" — массивом связей.`;

export function buildRelationsUser(ideasJson: string): string {
  return `Вот список идей из книги (в формате JSON):

---
${ideasJson}
---

Определи связи между этими идеями.`;
}
